#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	if (c != '\0')
	{
		while (s[i])
		{
			if (s[i] == c)
				return ((char *)&(s[i]));
			i++;
		}
	}
	else
	{
		while (s[i])
			i++;
		return ((char *)&(s[i]));
	}
	return (NULL);
}
