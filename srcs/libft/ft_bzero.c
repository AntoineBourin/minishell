#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*cpy;

	cpy = s;
	i = 0;
	if (s == NULL)
		return ;
	while (i < n)
	{
		cpy[i] = 0;
		i++;
	}
}
